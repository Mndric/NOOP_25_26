public class GarageDoor {
    public void open(){
        System.out.println("Opening garage door!");
    }
    public void lightOn(){
        System.out.println("Garage light is on!");
    }
    public void close(){
        System.out.println("Closing garage door!");
    }
    public void lightOff(){
        System.out.println("Turning off garage light!");
    }

}
